/*------Constants for rnd_uni()---------------------------------------*/
#define INF 1.0e14

#define IM1 2147483563
#define IM2 2147483399
#define AM (1.0/IM1)
#define IMM1 (IM1-1)
#define IA1 40014
#define IA2 40692
#define IQ1 53668
#define IQ2 52774
#define IR1 12211
#define IR2 3791
#define NTAB 32
#define NDIV (1+IMM1/NTAB)
#define EPS 1.2e-7
#define RNMX (1.0-EPS)

//generate a uniformly distributed random number in [0,1)
double rnd_uni_gen()
{
	static long idum2 = 123456789;
	static long iy = 0;
	static long iv[NTAB];

	long j;
	long k;
	double temp;

	if (rnd_uni_init <= 0) {
		if (-(rnd_uni_init) < 1) rnd_uni_init = 1;
		else rnd_uni_init = -(rnd_uni_init);
		idum2 = (rnd_uni_init);
		for (j = NTAB + 7; j >= 0; j--) {
			k = (rnd_uni_init) / IQ1;
			rnd_uni_init = IA1 * (rnd_uni_init - k * IQ1) - k * IR1;
			if (rnd_uni_init < 0) rnd_uni_init += IM1;
			if (j < NTAB) iv[j] = rnd_uni_init;
		}
		iy = iv[0];
	}
	k = (rnd_uni_init) / IQ1;
	rnd_uni_init = IA1 * (rnd_uni_init - k * IQ1) - k * IR1;
	if (rnd_uni_init < 0) rnd_uni_init += IM1;
	k = idum2 / IQ2;
	idum2 = IA2 * (idum2 - k * IQ2) - k * IR2;
	if (idum2 < 0) idum2 += IM2;
	j = iy / NDIV;
	iy = iv[j] - idum2;
	iv[j] = rnd_uni_init;
	if (iy < 1) iy += IMM1;
	if ((temp = AM * iy) > RNMX) return RNMX;
	else return temp;
}/*------End of rnd_uni()--------------------------*/

