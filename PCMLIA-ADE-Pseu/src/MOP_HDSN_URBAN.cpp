static int LOS(int i, int j, double h1, int a, int b, double h2)
{
	double h = h1;
	double cc = h2;
	int m;
	int x, y, x1, y1;
	double z1, z2,
		z;  //z?????±??é?μ????è   x?￠y?aá?μ?oá×Y×?±ê2?￡¨?y??êy￡? x1?aμ￥??2?á?￡¨′?・?o?￡? z1?￠z2μ?D?μ?×?±êμ????è
	double k, k1;          //á?μ?3é??μ?D±?ê
	double x2;            //ó?óú′?′￠?ù?óμ?×?±ê
	double y2;
	int x3;
	int y3;             //??y2??D??ˉ
	x = abs(a - i);    //á?μ???μ?×?±ê2?
	y = abs(b - j);
	if (x >= y) {
		if (i != a) {
			x1 = (a - i) / x;         //′??y?oêy
			k = ((double)(b - j)) / ((double)(a - i));
			k1 = ((double)(cc - h)) / (a - i);
			for (m = 1; m < x; m++) {
				x2 = i + x1 * m;
				y2 = k * (x2 - i) + j;
				z = k1 * (x2 - i) + h; ////x?￠y?￠z×?±ê￡¨?±??￡?
				if (y2 == (int)y2) {
					x3 = (int)x2;
					y3 = (int)y2;
					z1 = map[x3][y3];
					if (z < z1) {
						return 0;
					}
				}
				else {
					x3 = (int)x2;
					y3 = (int)y2;
					z1 = map[x3][y3];
					y3 = y3 + 1;//不会越界
					z2 = map[x3][y3];
					if (z < z1 || z < z2) {
						return 0;
					}
				}
			}
		}
	}
	else {
		if (j != b) {
			y1 = (b - j) / y;         //′??y?oêy
			k = ((double)(a - i)) / ((double)(b - j));
			k1 = ((double)(cc - h)) / (b - j);
			for (m = 1; m < y; m++) {
				y2 = j + y1 * m;
				x2 = k * (y2 - j) + i;
				z = k1 * (y2 - j) + h;
				if (x2 == (int)x2) {
					x3 = (int)x2;
					y3 = (int)y2;
					z1 = map[x3][y3];
					if (z < z1) {
						return 0;
					}
				}
				else {
					x3 = (int)x2;
					y3 = (int)y2;
					z1 = map[x3][y3];
					x3 = x3 + 1;//不会越界
					z2 = map[x3][y3];
					if (z < z1 || z < z2) {
						return 0;
					}
				}
			}
		}
	}
	return 1;
}
